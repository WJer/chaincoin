<template>
    <div class="page-recharge">
        <div class="recharge-head">
            充值货币
        </div>
        <div class="recharge-total">
            {{`${count}${coin.name}`}}
        </div>
        <div class="recharge-head">
            充值二维码
        </div>
        <div class="recharge-code"></div>
        <mt-button type="primary" size="large" class="btn-submit">保存</mt-button>
        <div class="recharge-address">
            <span>{{coin.address}}</span>
            <a class="btn-copy">复制</a>
        </div>
        <mt-button type="primary" size="large" class="btn-complete" @click="_complete">完成充值</mt-button>
	</div>
</template>
<script>
export default {
    props: {
		coin: {
			type: Object,
			default: null
		},
		count: {
			type: Number,
			default: 0
        },
        cycle: {
            type: Number,
			default: 0
        },
        money: {
            type: Number,
			default: 0
        },
        rate: {
            type: Number,
			default: 0
        },
        coupon: {
            type: String,
			default: ''
        }
	},
    methods: {
        _complete () {
            this.util.api.get('/saveMortgage', {
                params: {
                    coin: this.coin.name,
                    coinNumber: this.count,
                    cycle: this.cycle,
                    money: this.money,
                    rate: this.rate,
                    exponent: this.coin.price,
                    couponId: this.coupon,
                    mortgageRate: this.coin.mortgateRate,
                    address: this.coin.address
                }
            }).then((res) => {
                if (res && res.result) {
                    CC.popSlide();
                    CC.$router.push('/approval');
                }
            })
        }
    }
}
</script>
